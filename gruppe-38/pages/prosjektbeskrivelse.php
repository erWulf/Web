<h2>Prosjektbeskrivelse</h2>
<h3>Bakgrunn for prosjektet</h3>
<p>
	Kulturfestivalen Kulquinox i Sandvika trenger en ny og moderne webside. <a href="http://www.kulquinox.no">Den eksisterende</a> er vanskelig å navigere og tilgangen til relevant informasjon er dårlig organisert. Festivalen arrangeres  to ganger i året, med støtte av kommunen, og med en ny webløsning på plass vil festivalen potensielt få flere betalende gjester og større kommersiell suksess.
</p>
<h3>Problemstilling</h3>
<p>
	Kulquinox sine nettsider er vanskelig å søke opp på Google og harmonerer dårlig med dagens standarder og forventninger for hva en informasjonsside skal tilby. Dette er et uutnyttet potensiale for festivalens markedsføring. Websiden mangler dessuten universell utforming, som igjen utelukker grupper med nedsatte funksjonsevner å finne relevant informasjon om arrangementet. Festivalen har også hatt få besøkende.
</p>
<h3>Målgruppe</h3>
<p>
	Kulturinteresserte folk og barnefamilier i Sandvika, Oslo og omegn. Turister/reisende som er i regionen under festivalen.
</p>
<h3>Mål</h3>
<p>
	Vi ønsker å lage et forslag til ny webløsning som kan overta for den eksisterende før året er omme. Den skal tilpasses dagens krav for universell utforming samtidig ha en funksjonell og estetisk design. Øke antall besøkende for festivalen.
</p>
<h3>Produktmål</h3>
<ul>
	<li>Informasjon om tid og sted.</li>
	<li>Informasjon om billetter, pris, hvor de kan kjøpes.</li>
	<li>Lage en oversiktlig programside hvor det kan legges inn informasjon om:
		<ul>
			<li>Artister/band</li>
			<li>Film</li>
			<li>Teater</li>
			<li>Kunst/Utstillinger</li>
			<li>Foredrag som skal holdes</li>
		</ul></li>
	<li>Tilpasse webløsningen for sosiale medier</li>
	<li>Lage et galleri med fotografier fra tidligere arrangementer</li>
	<li>Beskrivelse av festivalens bakgrunn og historie</li>
	<li>Lage en løsning for å sende inn spørsmål til festivalen via skjema</li>
	<li>Informasjon for gjester med handicap</li>
</ul>
<h3>Prosessmål</h3>
<p>
	Lære prosjektarbeid og hvordan man jobber sammen i gruppe. Forstå hvordan man tar i bruk HTML, CSS og PHP for å utvikle moderne webløsninger.
</p>
