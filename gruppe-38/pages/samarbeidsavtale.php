<h2>Samarbeidsavtale Gruppe 38 webprosjekt</h2>
<p>
	Mellom: Mathias Groven, Nicolai Grøndahl, Erlend Gunnesen og Martin Grini
</p>
<p>
	Inngått den 31/10 - 2011
</p>
<h3>1. Bakgrunn</h3>
<p>
	Som en del av studiet ved  Høgskolen i Oslo og Akershus har studentene fått i oppgave å gjennomføre et prosjekt hvor det skal opprettes en web side.
</p>
<p>
	Prosjektet skal gjennomføres som et gruppearbeid. Gruppe 38, som er sammensatt av Mathias Groven, Nicolai Grøndahl, Erlend Gunnesen og Martin Grini har besluttet å inngå denne samarbeidsavtalen for å klargjøre ansvars og oppgavedeling i gruppen, fastholde hvordan arbeidet skal gjennomføres og hvilke spilleregler som skal gjelde for å sikre et best mulig resultat i prosjektet.
</p>
<h3>2. Formål</h3>
<p>
	Formålet med denne avtalen er å sikre at målet vårt blir oppnådd på den måten og med de retningslinjene vi har satt.
	Siden vi ikke har valgt gruppene selv så er vi ikke satt sammen med tanke på forkunnskaper, men med tanke på at vi kommer fra forskjellige studier og dermed har forskjellige kunnskaper.
</p>
<p>
	Gjennom samarbeidet skal vi utnytte vår forskjellige bakgrunn og kompetanse, til å skape et best mulig samlet resultat i prosjektet.
</p>
<p>
	For å bidra til dette, inneholder også samarbeidsavtalen noen regler som vi selv har fastsatt og som gjelder for alle medlemmene av gruppa. Denne avtalen sikrer at disse blir ivaretatt og at alle er klar over hvilke regler som gjelder.
</p>
<p>
	Vi har fordelt oppgaver med tanke på hva gruppas medlemmer ønsker å sette seg inn i /jobbe med, men også med tanke på hva den enkelte har av forkunnskaper og kan bidra med.
</p>
<h3>3. Arbeidsmetode</h3>
<p>
	Arbeidet gjennomføres ved at vi fordeler oppgaver som vi løser individuelt og koordinerer i møter.  Etter de første to møtene har vi valgte vi å sette opp to faste tidspunkter hver uke for gruppemøte.
	På disse møtene ser vi på hva vi har gjort siden sist gang. Hvordan vi skal jobbe videre, avdekker eventuelt forsinkelse og avtaler hvordan vi kan ta igjen dette, eller om det er ting vi må gjøre annerledes.
</p>
<p>
	Vi avtaler også hvilke oppgaver hver enkelt skal ha ferdig til tidsfrister vi setter(f.eks.: neste møte) eller i forhold til milepælsplanen vår.
	For å holde alle oppdatert til en hver tid bruker vi en delt mappe alle har tilgang til îliveî <a href="http://www.dropbox.com">Dropbox</a>, i tillegg til at alle har prosjektet lagret på egen maskin i tilfelle en eller flere av oss skulle miste verdifull data.
</p>
<p>
	Vi jobber også to og to på de mest vesentlige oppgavene for å sikre at vi alltid har fremgang i prosjektet, selv om en eller to av oss ikke skulle ha mulighet til å møte opp på gruppemøtet. Vi har en fast sekretær som skriver møtereferater for alle møter og legger det ut i mappa alle har tilgjengelig. Slik at de som ikke har hatt mulighet til å møte opp, kan lese hva vi har diskutert og gått igjennom.
</p>
<h3>4. Oppgavefordeling</h3>
<p>
	Oppgavene er fordelt med tanke på forkunnskaper og hva den enkelte ønsker å sette seg inn i/ jobbe med. Disse oppgavene er skikkelig beskrevet og spesifisert under <a href="./ansvarskart.php">ansvarskartet</a>. Dette er for å sikre at alle alltid vet hvilke oppgaver de skal jobbe med når. Tidsfrister for når ting må være ferdig er spesifisert i milepælsplan og i møtereferatene. På de viktigste og mest vesentlige oppgavene er vi alltid to som jobber, der en er hovedansvarlig for den gitte oppgaven. Dette er for å skire at vi alltid har fremgang i prosjektet om noen ikke skulle ha mulighet til å møte opp, men også fordi vi får et bredere spekter av kunnskap og input når to jobber med samme sak. 
</p>
<p>
	Vi har en fast:
</p>
<ul>
	<li>
		Gruppeleder<br/>
		som leder prosjektet, og som har ansvar for å ta viktige avgjørelser om det skulle oppstå uenigheter. Gruppeleder har også ansvar for å reservere grupperom til de møtene hvor vi skal ta i bruk det. gruppeleder har også ansvar for å gi alle beskjed på sms eller epost ved eventuell avlysning av møter, endrede møtetider eller om det settes opp et ekstra møte.
	</li>
</ul>
<ul>
	<li>
		Sekretær<br/>
		som er ansvarlig for å legge ut møtereferater etter hvert møte, så personer som ikke har hat mulighet til å møte opp på det aktuelle møte kan lese seg opp på hva som har blitt diskutert.
	</li>
</ul>
<h3>5. Ressurser, tidsforbruk og kompetanse</h3>
<p>
	Gruppa er studenter ved Høgskolen i Oslo og Akershus og resultatet av arbeidet vil inngå som en del av vurderingsgrunnlag og karaktersetting for hvert enkelt medlem av prosjektgruppa.  Gruppas medlemmer er enige om at det er viktig at alle gruppas medlemmer føler et felles ansvar for å skape et godt resultat, og at alle bidrar med en tilnærmet like stor arbeidsinnsats og antall timeverk.  Gruppa er også enige om at man skal bistå hverandre på den måten at dersom enkelte medlemmer har større forutsetninger eller kunnskaper for å løse visse oppgaver, så skal vedkommende være villig til å påta seg denne oppgaven, eller gi råd til de andre i gruppa for å sikre best mulig kvalitet i den endelige løsningen. 
</p>
<h3>6. Tidsperspektiv og fremdrift</h3>
<p>
	De overordnede tidsfristene i prosjektet er fastlagt gjennom at tre hovedleveranser. Disse tider vi har visst om helt fra dette prosjektet startet.
</p>
<p>
	Vi har også satt opp en egen milepælsplan der mindre oppgaver/ milepæler er spesifisert under: <a href="./aktivitetsplan.php">aktivitetsplan</a> og <a href="./milepael.php">milepælsplan</a>. 
</p>
<p>
	Vi har satt opp faste møtetider så det alltid er lett å vite for hver enkelt når man må ha gjort fremgang med det de har ansvar for. Etter hvert møte legges det ut et møtereferat i mappa alle har tilgjengelig, her er det spesifisert oppgaver vi skal jobbe med til neste møte.
</p>
<p>
	Ved forsinkelser eller avvik i forhold til fremdriftsplanen som gjennomgås i hvert møte, avtales tiltak og ansvar for å rette opp fremdriften med målsetting å komme tilbake på planen.  Avtalte tiltak og ansvar noteres i møtereferatet og følges opp i neste møte.
</p>
<h3>7. Oppmøte og regler for samarbeidet</h3>
<p>
	Tiden vi møtes er satt opp etter diskusjon og tilpasning for alle i gruppa, derfor forventes det at alle møte opp til de avtalte tidene. Om man likevel ikke skulle kunne møte opp har vi satt en regel som sier at personen det gjelder må melde ifra gruppeleder eller sekeretær, senest 1 time før møtestart. Om en person gjentatte ganger ikke skulle møte opp til avtalte tider eller ikke gjøre avtalte oppgaver uten en gyldig grunn (vi vet at uforutsette ting kan skje), skjer følgende:
</p>
<p>
	Personen som ikke gjør tildelte oppgaver innen fastsatt tid, og dermed hindrer prosjektet i å gjøre fremdrift, kan miste noen av de tildelte arbeidsoppgavene sine(som noen andre i gruppa da må ta over) og/eller person får en muntlig advarsel av gruppeleder, med mulighet for å forbedre seg.
</p>
<p>
	Hvis personen likevel ikke skulle forbedre seg/ gjøre bedre innsats så skal de tre andre i gruppa diskutere og avgjøre om person skal utestenges fra prosjektet, noe som vil si at personen ikke lengere er en del av gruppearbeidet eller gruppeleveringene.
</p>
<p>
	Om en person minste noen av sine arbeidsoppgaver grunnet dårlig arbeidsinnsats så spesifiseres dette i sluttrapporten til gruppa.
</p>
